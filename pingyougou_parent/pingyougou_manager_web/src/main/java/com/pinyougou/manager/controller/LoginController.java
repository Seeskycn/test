@RestController
@RequestMapping("/login")
public class LoginController {

    @RequestMapping("/name")
    public Map username(){
        String name = SecurityContextHolder.getContext().getAuthentication().getName();
        Map<String ,String > map = new HashMap<>();
        map.put("loginName", name);
        return map;
    }
}
